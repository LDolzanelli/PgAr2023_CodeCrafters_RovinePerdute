package it.unibs.fp.rovineperdute;

public class Coordinates {
    private int x;
    private int y;
    private int h;

    public Coordinates(int x, int y, int h) {
        this.x = x;
        this.y = y;
        this.h = h;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getH() {
        return h;
    }
}
